// Run when the UI is created.
void SetEditFieldRequiredState(HWND hDlg, BOOL newRequiredState)
{
    HRESULT hr = S_OK;

    if (_pAccPropServices == NULL)
    {
        hr = CoCreateInstance(
            CLSID_AccPropServices,
            nullptr,
            CLSCTX_INPROC,
            IID_PPV_ARGS(&_pAccPropServices));
    }

    if (SUCCEEDED(hr))
    {
        VARIANT var;
        var.vt = VT_BOOL;
        var.boolVal = VARIANT_TRUE;

        // Mark the edit field as being required.
        hr = _pAccPropServices->SetHwndProp(
            GetDlgItem(hDlg, IDC_EDIT_NAME),
            OBJID_CLIENT,
            CHILDID_SELF,
            IsRequiredForForm_Property_GUID,
            var);
    }
}

// Run when the UI is destroyed.
void ClearEditFieldRequiredState(HWND hDlg)
{
    if (_pAccPropServices != nullptr)
    {
        // We only added the one property to the hwnd.
        MSAAPROPID props[] = { IsRequiredForForm_Property_GUID };
        HRESULT hr = _pAccPropServices->ClearHwndProps(
            GetDlgItem(hDlg, IDC_EDIT_NAME),
            OBJID_CLIENT,
            CHILDID_SELF,
            props,
            ARRAYSIZE(props));

        _pAccPropServices->Release();
        _pAccPropServices = NULL;
    }
}
